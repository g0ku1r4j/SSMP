.MODEL SMALL
.STACK 100H
.DATA
	A DW 1234H
	B DW 5678H
.CODE
	MOV AX, @DATA
	MOV DS, AX
	MOV SI, OFFSET A
	MOV BX, [SI]
	MOV DI, OFFSET B
	MOV AX, [DI]
	ADD AX, BX
	MOV CX, 0000H
	MOV BX, 16
	CONVERT:
		XOR DX, DX
		DIC BX
		PUSH DX
		INC CX
		CMP AX, 0
		JNZ CONVERT
	PRINT:
		POP DX
		ADD DX, '0'
		CMP DX, '9'
		JBE OUTPUT
		ADD DX, 7
	OUTPUT:
		MOV AH, 02H
		INT 21H
		DEC CX
		JNZ PRINT
	MOV AH, 4CH
	INT 21H
END
